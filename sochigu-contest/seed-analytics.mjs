async function main() {
  console.log('═══════════════════════════════════════════════════');
  console.log('  SochiGU Contest — Seed данных аналитики (v2)    ');
  console.log('═══════════════════════════════════════════════════');

  // 1. Логин / создание admin
  log('1. Вход как администратор...');
  let adminLogin = await post('/auth/login', { email: ADMIN.email, password: ADMIN.password });
  if (!adminLogin?.accessToken) {
    await post('/auth/register', ADMIN);
    sql(`UPDATE users SET role='admin' WHERE email='${ADMIN.email}';`);
    adminLogin = await post('/auth/login', { email: ADMIN.email, password: ADMIN.password });
  }
  if (!adminLogin?.accessToken) {
    console.error('❌ Не удалось войти как admin. Backend запущен на порту 3000?');
    process.exit(1);
  }
  const adminToken = adminLogin.accessToken;
  ok('Администратор авторизован');

  // 2. Создаём номинации
  log('2. Создаём номинации...');
  for (const nom of NOMINATIONS) {
    const r = await post('/nominations', nom, adminToken);
    if (r) ok(`«${nom.name}»`);
  }

  // Получаем ID номинаций
  const nomsRes = await fetch(BASE + '/nominations');
  const allNoms = await nomsRes.json().catch(() => []);
  if (allNoms.length < 8) {
    console.error('❌ Номинации не созданы. Убедись что бэкенд запущен.');
    process.exit(1);
  }
  // Индексируем по порядку создания (sortOrder 1..8)
  const nomIds = allNoms
    .sort((a, b) => a.sortOrder - b.sortOrder)
    .map(n => n.id);

  // 3. Регистрируем участников
  log('3. Регистрируем участников (40 человек)...');
  const tokens = [];
  process.stdout.write('  ');
  for (let i = 0; i < PARTICIPANTS.length; i++) {
    const p = PARTICIPANTS[i];
    const email = `participant_${i + 1}@seed.sochigu.ru`;
    const password = 'Seed1234!';
    await post('/auth/register', {
      email, password,
      firstName: p.f, lastName: p.l,
      university: p.u, faculty: p.fac,
      course: p.course, city: p.c,
      phone: `+7(9${String(i).padStart(2, '0')})555-${String(i * 7 % 100).padStart(2, '0')}-${String(i * 13 % 100).padStart(2, '0')}`,
    });
    await sleep(300);
    const login = await post('/auth/login', { email, password });
    tokens.push(login?.accessToken || null);
    dot();
    await sleep(200);
  }
  console.log('');
  ok(`${PARTICIPANTS.length} участников готовы`);

  // 4. Создаём заявки и отправляем
  log(`4. Создаём ${APPLICATIONS.length} заявок...`);
  process.stdout.write('  ');
  const appIds = [];
  for (const [pIdx, nIdx, title, keywords] of APPLICATIONS) {
    const token = tokens[pIdx];
    if (!token) { dot(); continue; }
    const nomId = nomIds[nIdx];
    if (!nomId) { dot(); continue; }

    const app = await post('/applications', {
      nominationId: nomId,
      projectTitle: title,
      projectDescription: `Описание проекта «${title}». Актуальность, методология и ожидаемые результаты работы.`,
      keywords,
      teamMembers: [{ name: `${PARTICIPANTS[pIdx].f} ${PARTICIPANTS[pIdx].l}`, role: 'Руководитель', email: `participant_${pIdx + 1}@seed.sochigu.ru` }],
      supervisor: { name: 'Научный руководитель', title: 'Доцент кафедры', email: 'supervisor@sochigu.ru' },
    }, token);

    if (app?.id) {
      await sleep(400);
      await post(`/applications/${app.id}/submit`, {}, token);
      appIds.push({ id: app.id, daysAgo: APPLICATIONS[appIds.length][4] });
    }
    dot();
    await sleep(400);
  }
  console.log('');
  ok(`${appIds.length} заявок создано и отправлено`);

  // 5. Расставляем даты подачи через SQL (для красивого timeline)
  log('5. Расставляем даты подачи...');
  const base = new Date('2025-10-30');
  for (const { id, daysAgo } of appIds) {
    const d = new Date(base);
    d.setDate(d.getDate() - daysAgo);
    // Добавляем случайный разброс ±2 дня
    d.setDate(d.getDate() + (Math.floor(Math.random() * 5) - 2));
    const iso = d.toISOString().slice(0, 19).replace('T', ' ');
    sql(`UPDATE applications SET \\"submittedAt\\"='${iso}' WHERE id='${id}';`);
  }
  ok('Даты обновлены');

  // 6. Создаём новости (если нет)
  log('6. Создаём новости...');
  await sleep(1000);
  const news1 = await post('/news', {
    title: 'Открыт приём заявок на конкурс СочиГУ 2026',
    slug: 'priyom-zayavok-2026',
    excerpt: 'Стартап-студия СочиГУ объявляет о начале приёма заявок на ежегодный конкурс.',
    content: 'Приглашаем студентов и аспирантов всех курсов подать заявку до 30 октября.',
    isPublished: true,
  }, adminToken);
  if (news1) ok('«Открыт приём заявок»');

  const news2 = await post('/news', {
    title: 'Встреча с экспертами: как защитить проект',
    slug: 'vstrecha-eksperty-zashchita',
    excerpt: 'В ноябре пройдёт серия консультаций с членами Экспертного совета.',
    content: 'Регистрация открыта в личном кабинете. Встречи пройдут в онлайн-формате.',
    isPublished: true,
  }, adminToken);
  if (news2) ok('«Встреча с экспертами»');

  // Итог
  console.log('\n═══════════════════════════════════════════════════');
  console.log('  ✅ Готово! Данные загружены.');
  console.log('═══════════════════════════════════════════════════');
  console.log('');
  console.log('  Дашборд:  http://localhost:5173/admin/analytics');
  console.log('');
  console.log('  👑 Администратор:');
  console.log('     admin@sochigu.ru / Admin1234');
  console.log('');
  console.log('  📊 Что создано:');
  console.log(`     • 8 номинаций`);
  console.log(`     • ${PARTICIPANTS.length} участников (10 вузов, 10 городов)`);
  console.log(`     • ${appIds.length} заявок (сентябрь–октябрь 2025)`);
  console.log('     • Облако из 50+ уникальных ключевых слов');
  console.log('');
}

