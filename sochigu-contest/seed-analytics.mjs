import { execSync } from 'child_process';

const BASE = 'http://localhost:3000/api';

// ─── Хелперы ────────────────────────────────────────────────────────────────

async function post(path, body, token) {
  const res = await fetch(BASE + path, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      ...(token ? { Authorization: `Bearer ${token}` } : {}),
    },
    body: JSON.stringify(body),
  });
  const text = await res.text();
  let data;
  try { data = JSON.parse(text); } catch { data = { raw: text }; }
  if (!res.ok) {
    console.warn(`  ⚠  POST ${path} → ${res.status}:`, JSON.stringify(data).slice(0, 120));
    return null;
  }
  return data;
}

async function patch(path, body, token) {
  const res = await fetch(BASE + path, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${token}`,
    },
    body: JSON.stringify(body),
  });
  const text = await res.text();
  let data;
  try { data = JSON.parse(text); } catch { data = { raw: text }; }
  if (!res.ok) {
    console.warn(`  ⚠  PATCH ${path} → ${res.status}:`, JSON.stringify(data).slice(0, 120));
    return null;
  }
  return data;
}

function sql(query) {
  try {
    execSync(
      `docker exec sochigu_db psql -U sochigu -d sochigu_contest -c "${query}"`,
      { stdio: 'pipe' }
    );
  } catch (e) {
    console.warn('  ⚠  SQL:', e.stderr?.toString()?.slice(0, 100) || e.message);
  }
}

